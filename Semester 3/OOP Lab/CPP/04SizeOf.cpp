/* C++ Program to Find Size of int, float, double and char of Your System */
#include <iostream>
using namespace std;

int main()
{
    cout << "Size of \n";
    cout << " int: " << sizeof(int) << "\n";
    cout << " float: " << sizeof(float) << "\n";
    cout << " double: " << sizeof(double) << "\n";
    cout << " char: " << sizeof(char) << "\n";
    return 0;
}